      Алкоголизм и наркомания
      Священники часто сталкиваются с этими социально-духовными недугами общества. Сегодня для химически зависимых
      людей и их родственников при многих храмах созданы группы реабилитации, братства и сестричества трезвости,
      семейные клубы. Есть различные программы помощи. Об одной из них, о том, как можно организовать работу и
      каких
      ошибок избегать, мы беседуем с духовным руководителем программ психологической и социальной реабилитации
      алко
