      Алкоголизм — хроническое заболевание, поэтому об окончательном выздоровлении говорить неуместно. Можно
      говорить о тех, кто живет при этом хроническом недуге, сохраняя трезвость, и таких у нас немало. Очень важно
      понять, что мы имеем дело со сложным прогрессирующим заболеванием, которое может возвращаться после огромных
      перерывов, после ремиссий в 20–30 лет. Может менять формы проявления. С точки зрения биологии у алкоголика
      изменяются биохимические процессы головного мозга и формируется стойкая химическая зависимость.
      Организм требует постоянного поступления определенных веществ. Принимая даже небольшую дозу химического
      вещества, наркотика или алкоголя, человек теряет контроль над собой — запускает алкогольные механизмы,
