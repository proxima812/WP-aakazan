      Я глубоко убежден, что должны приветствоваться любые средства, которые помогают человеку избавиться от
      алкоголизма и наркомании, если эти средства не связаны с отказом от христианской веры и христианских
      убеждений. Не приветствуется только то, что наносит вред духовному, психическому и физическому здоровью
      человека, например колдовство, оккультизм, магия, а также то, что отрывает человека от веры, от благодати.
      Поскольку алкоголизм, как и прочие виды зависимостей, — грех и болезнь одновременно, нужно, чтобы общество и
      Церковь боролись против него совместными усилиями. Негативное отношение духовенства к программе «12 шагов»
      зависит от степени осведомленности… Никаких богословских предпосылок отвергать программу нет.
      Против вина не показывай себя храбрым, ибо многих погубило вино (Сир. 3, 29)
      <br><br>
      Митрополит Волоколамский Илларион
      Смысл этой программы («12 шагов») заключается в том, что она предлагает не только освобождение от
      последствий
      алкоголизма или наркомании, но и врачует сам корень болезни, освобождая человека от той внутренней пустоты,
      которая является причиной «зависимости». Программа эта рассчитана на постепенное осознание человеком
      бесперспективности своей прежней жизни, необходимости исправиться, собственного бессилия и необходимости
      какой-то Высшей Силы, которая помогла бы ему духовно и нравственно переродиться. Иными словами, это
      программа
      всецелого исцеления на всех уровнях: физическом, душевном и духовном. Эта программа имеет религиозный
      наполнитель; на каком-то этапе она приводит человека к Богу.
      <br><br>
      «Старый свет»
      (Христианский общественный благотворительный фонда)
      психолог, председатель правления — Евгений Проценко
      Христианский общественный благотворительный фонд «Старый Свет» около 25 лет работает в области профилактики
      и
      реабилитации зависимости — алкоголизма и наркомании.
      Основываясь на системном анализе собранных данных об эффективных подходах в этой области и на практике
      собственной многолетней работы, я с полной ответственностью свидетельствую, что среди возможных средств
      решения этих задач в нашей стране абсолютно вне конкуренции находятся группы «Анонимных алкоголиков»,
      «Анонимных наркоманов», «Анонимных игроков» и другие подобные сообщества. Именно эти движения, опираясь на
      десятилетиями выверенную и беспрецедентно эффективную методологическую основу — «Программу 12 шагов»,
      являются, по моему убеждению, единственным реальным ресурсом в нашей ситуации. Почему? Прежде всего потому,
      что они не требуют абсолютно никаких финансовых затрат со стороны государства или других источников. Также
      эти
      движения направлены на самовоспроизводство, а значит, могут (опять-таки, без всяких материальных затрат)
      создавать целостные системы поддержки, в том числе — и в тех регионах, областях или населенных пунктах, где
      такая поддержка отсутствует полностью.
      Что же касается духовной составляющей этого подхода, то является несомненным (и это подтверждается
      множеством
      виднейших отечественных и зарубежных богословов и пастырей), что она опирается исключительно на принципы
      христианской аскетики (причем в понимании, наиболее близком традициям православия). Возникающие же у
      отдельных
      представителей Православной (и не только) Церкви возражения против этой программы основаны, в подавляющем
      большинстве случаев, на совершенно недостаточном или абсолютно предвзятом знакомстве с ее принципами.
      Никакой
      серьезной дискуссии с подобными «возражателями» (а порой — и «запретителями») за все время не получается,
      чаще
      всего — потому, что к подобной дискуссии эти люди не готовы, предпочитая такой полемике эмоциональные,
      бездоказательные и зачастую абсолютно несуразные обвинения в адрес программы.
